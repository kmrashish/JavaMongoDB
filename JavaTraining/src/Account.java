
public class Account {
  private double inBalance;
  private double finBalance;
  
  public Account(double in, double fin)
  {
	  this.inBalance=in;
	  this.finBalance=fin;
  }
  public double getInBalance()
  {
	  return inBalance;
  }
  public double getFinBalance()
  {
	  return finBalance;
  }
}
